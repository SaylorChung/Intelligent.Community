namespace Saylor.Storage
{
    /// <summary>
    ///     表示排序类型。
    /// </summary>
    public enum SortOrder
    {
        /// <summary>
        ///     Indicates that the sorting style is not specified.
        /// </summary>
        un = -1,
        /// <summary>
        ///     Indicates an ascending sorting.
        /// </summary>
        asc = 0,
        /// <summary>
        ///     Indicates a descending sorting.
        /// </summary>
        desc = 1
    }
}
